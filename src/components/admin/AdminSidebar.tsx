export default function AdminSidebar() {
  const pathname = usePathname();
  const router = useRouter();
  const [mobileOpen, setMobileOpen] = useState(false);
  const [storeName, setStoreName] = useState("");

  const fetchStoreName = () => {
    fetch("/api/settings/public")
      .then((res) => res.json())
      .then((data) => {
        if (data.store_name) setStoreName(data.store_name);
      })
      .catch(() => {});
  };

  useEffect(() => {
    fetchStoreName();

    const handleSettingsUpdate = () => fetchStoreName();
    window.addEventListener("settings-updated", handleSettingsUpdate);
    return () => window.removeEventListener("settings-updated", handleSettingsUpdate);
  }, []);

  // 로그인 페이지에서는 사이드바를 숨김
  if (pathname === "/admin/login") return null;

  const handleLogout = async () => {
    const supabase = createClient();
    await supabase.auth.signOut();
    router.push("/admin/login");
    router.refresh();
  };

  const isActive = (href: string) =>
    href === "/admin" ? pathname === "/admin" : pathname.startsWith(href);

  const sidebar = (
    <div className="flex h-full w-60 flex-col border-r border-warm-gray-100 bg-white">
      <div className="border-b border-warm-gray-100 p-4">
        <h2 className="text-lg font-bold text-warm-gray-800">{storeName ? `${storeName} 관리자` : "관리자"}</h2>
      </div>
      <nav className="flex-1 space-y-1 p-3">
        {navItems.map((item) => (
          <Link
            key={item.href}
            href={item.href}
            onClick={() => setMobileOpen(false)}
            className={`flex items-center gap-3 rounded-lg px-3 py-2.5 text-sm transition-colors ${
              isActive(item.href)
                ? "bg-primary-50 text-primary-600 font-medium"
                : "text-warm-gray-600 hover:bg-warm-gray-50"
            }`}
          >
            {icons[item.icon]}
            {item.label}
          </Link>
        ))}
      </nav>
      <div className="border-t border-warm-gray-100 p-3">
        <button
          onClick={handleLogout}
          className="flex w-full items-center gap-3 rounded-lg px-3 py-2.5 text-sm text-warm-gray-500 transition-colors hover:bg-warm-gray-50"
        >
          <svg className="h-5 w-5" fill="none" stroke="currentColor" viewBox="0 0 24 24">
            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={1.5} d="M17 16l4-4m0 0l-4-4m4 4H7m6 4v1a3 3 0 01-3 3H6a3 3 0 01-3-3V7a3 3 0 013-3h4a3 3 0 013 3v1" />
          </svg>
          로그아웃
        </button>
      </div>
    </div>
  );

  return (
    <>
      {/* 모바일 햄버거 */}
      <button
        type="button"
        onClick={() => setMobileOpen(true)}
        className="fixed left-4 top-4 z-40 rounded-lg border border-warm-gray-200 bg-white p-2 shadow-sm md:hidden"
      >
        <svg className="h-5 w-5 text-warm-gray-600" fill="none" stroke="currentColor" viewBox="0 0 24 24">
          <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M4 6h16M4 12h16M4 18h16" />
        </svg>
      </button>

      {/* 모바일 오버레이 */}
      {mobileOpen && (
        <div
          className="fixed inset-0 z-40 bg-black/30 md:hidden"
          onClick={() => setMobileOpen(false)}
        />
      )}

      {/* 모바일 사이드바 */}
      <div
        className={`fixed inset-y-0 left-0 z-50 transform transition-transform md:hidden ${
          mobileOpen ? "translate-x-0" : "-translate-x-full"
        }`}
      >
        {sidebar}
      </div>

      {/* 데스크탑 사이드바 */}
      <div className="hidden shrink-0 md:block">{sidebar}</div>
    </>
  );
}
